CREATE TRIGGER trg_update_password_change
AFTER UPDATE ON users
FOR EACH ROW
BEGIN
    IF NEW.password <> OLD.password THEN
        UPDATE settings
        SET last_password_change = CURRENT_TIMESTAMP()
        WHERE user_id = NEW.id;
    END IF;
END//

